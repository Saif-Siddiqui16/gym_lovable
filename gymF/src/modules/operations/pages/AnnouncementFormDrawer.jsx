import React, { useState } from 'react';
import { ChevronDown } from 'lucide-react';
import RightDrawer from '../../../components/common/RightDrawer';

const AnnouncementFormDrawer = ({ isOpen, onClose, onSuccess }) => {
    const [formData, setFormData] = useState({
        title: '',
        content: '',
        targetAudience: 'All Members',
        priority: 0,
        isActive: true
    });

    const [isSubmitting, setIsSubmitting] = useState(false);

    const handleSubmit = async (e) => {
        e.preventDefault();
        setIsSubmitting(true);

        // Simulating API call for demo behavior
        setTimeout(() => {
            setIsSubmitting(false);
            onSuccess?.();
            onClose();
        }, 1200);
    };

    return (
        <RightDrawer
            isOpen={isOpen}
            onClose={onClose}
            title="Create Announcement"
            subtitle="Create a new announcement for your members"
            maxWidth="max-w-2xl"
            footer={
                <div className="flex gap-3 w-full justify-end px-2">
                    <button
                        type="button"
                        onClick={onClose}
                        className="px-6 h-11 border-2 border-slate-100 bg-white text-slate-600 rounded-xl text-sm font-bold hover:bg-slate-50 transition-all active:scale-95"
                    >
                        Cancel
                    </button>
                    <button
                        type="submit"
                        form="announcement-form"
                        disabled={isSubmitting}
                        className="px-8 h-11 bg-indigo-600 text-white rounded-xl text-sm font-bold shadow-lg shadow-indigo-100 hover:bg-indigo-700 transition-all active:scale-95 disabled:opacity-50 flex items-center justify-center gap-2"
                    >
                        {isSubmitting ? (
                            <div className="w-4 h-4 border-2 border-white/20 border-t-white rounded-full animate-spin"></div>
                        ) : (
                            'Create Announcement'
                        )}
                    </button>
                </div>
            }
        >
            <div className="px-8 py-8">
                <form id="announcement-form" onSubmit={handleSubmit} className="space-y-8">

                    {/* Title Field */}
                    <div className="space-y-2">
                        <label className="block text-[10px] font-black text-slate-500 uppercase tracking-widest ml-1">Title *</label>
                        <input
                            required
                            type="text"
                            placeholder="Announcement title"
                            className="w-full h-11 px-4 rounded-xl border-2 border-slate-100 focus:border-indigo-500 focus:ring-4 focus:ring-indigo-500/10 text-sm font-semibold transition-all outline-none bg-slate-50/50"
                            value={formData.title}
                            onChange={(e) => setFormData({ ...formData, title: e.target.value })}
                        />
                    </div>

                    {/* Content Field */}
                    <div className="space-y-2">
                        <label className="block text-[10px] font-black text-slate-500 uppercase tracking-widest ml-1">Content *</label>
                        <textarea
                            required
                            rows={5}
                            placeholder="Announcement content..."
                            className="w-full p-4 rounded-xl border-2 border-slate-100 focus:border-indigo-500 focus:ring-4 focus:ring-indigo-500/10 text-sm font-semibold transition-all outline-none bg-slate-50/50 resize-none"
                            value={formData.content}
                            onChange={(e) => setFormData({ ...formData, content: e.target.value })}
                        />
                    </div>

                    {/* Target Audience */}
                    <div className="space-y-2">
                        <label className="block text-[10px] font-black text-slate-500 uppercase tracking-widest ml-1">Target Audience</label>
                        <div className="relative group">
                            <select
                                className="w-full h-11 px-4 rounded-xl border-2 border-slate-100 focus:border-indigo-500 focus:ring-4 focus:ring-indigo-500/10 text-sm font-semibold transition-all outline-none bg-slate-50/50 appearance-none cursor-pointer"
                                value={formData.targetAudience}
                                onChange={(e) => setFormData({ ...formData, targetAudience: e.target.value })}
                            >
                                <option>All Members</option>
                                <option>Active Members</option>
                                <option>Expired Members</option>
                                <option>Staff Only</option>
                                <option>Trainers Only</option>
                            </select>
                            <ChevronDown size={18} className="absolute right-4 top-1/2 -translate-y-1/2 text-slate-400 pointer-events-none group-focus-within:text-indigo-600 transition-colors" />
                        </div>
                    </div>

                    {/* Priority */}
                    <div className="space-y-2">
                        <label className="block text-[10px] font-black text-slate-500 uppercase tracking-widest ml-1">Priority (0-10)</label>
                        <input
                            type="number"
                            min="0"
                            max="10"
                            className="w-full h-11 px-4 rounded-xl border-2 border-slate-100 focus:border-indigo-500 focus:ring-4 focus:ring-indigo-500/10 text-sm font-semibold transition-all outline-none bg-slate-50/50"
                            value={formData.priority}
                            onChange={(e) => setFormData({ ...formData, priority: parseInt(e.target.value) || 0 })}
                        />
                    </div>

                    {/* Active Toggle */}
                    <div className="flex items-center justify-between p-4 bg-slate-50/30 border-2 border-slate-50 rounded-2xl group transition-all hover:bg-white hover:border-slate-100 shadow-sm">
                        <div className="space-y-0.5">
                            <p className="text-[13px] font-black text-slate-900 tracking-tight">Active</p>
                            <p className="text-[10px] font-bold text-slate-400 uppercase tracking-widest">Show announcement to members</p>
                        </div>
                        <button
                            type="button"
                            onClick={() => setFormData({ ...formData, isActive: !formData.isActive })}
                            className={`relative inline-flex h-7 w-12 shrink-0 cursor-pointer rounded-full border-2 border-transparent transition-colors duration-200 ease-in-out focus:outline-none ${formData.isActive ? 'bg-[#0f172a]' : 'bg-slate-200'
                                }`}
                        >
                            <span
                                aria-hidden="true"
                                className={`pointer-events-none inline-block h-6 w-6 transform rounded-full bg-white shadow-lg ring-0 transition duration-200 ease-in-out ${formData.isActive ? 'translate-x-5' : 'translate-x-0'
                                    }`}
                            />
                        </button>
                    </div>

                </form>
            </div>

            <style>{`
                .custom-scrollbar::-webkit-scrollbar { width: 5px; }
                .custom-scrollbar::-webkit-scrollbar-track { background: transparent; }
                .custom-scrollbar::-webkit-scrollbar-thumb { background: #e2e8f0; border-radius: 10px; }
            `}</style>
        </RightDrawer>
    );
};

export default AnnouncementFormDrawer;
